public class OperadorTernario {
    public static void main(String[] args) {
        /* Operador Ternario (abreviación if else)
            Está formado por tres partes o elementos
            1. Evaluación booleana (condición)
            2. Resultado SI se cumple la condición
            3. Resultado si NO se cumple la condición
        */

        // variable = condición ? verdadero: falso

        String numMayor = 100 > 50 ? "El primero es mayor" : "El segundo es mayor";

        System.out.println(numMayor);



    }
}
